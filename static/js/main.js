loadPage = (index, title) => {
    if(index >= 10){
        switch (index){
            case 10:
                window.location.href = "/ds-content/fib.php?t=" + encodeURI(title);
                break
            case 11:
                    window.location.href = "/ds-content/fct.php?t=" + encodeURI(title);
                break
            case 12:
                    window.location.href = "/ds-content/toh.php?t=" + encodeURI(title);
                break
            case 13:
                window.location.href = "/ds-content/lists.php?t=" + encodeURI(title);
                break
            default:
                alert('Page not found. Contact Management')
        }

    }else {
        setTimeout(()=>{
            window.location.href = "/ds-content/content-data.php?dat=" + index + "&t=" + encodeURI(title);
        }, 1000);
    }
}
user_data = (data) => {
    if(data == 'random'){
        $('.user_random').css({
            display: 'block',
        })
        $('.user_def').css({
            display: 'none',
        })
        $('.user_elem').css({
            display: 'none',
        })
    } else if(data == 'user_def'){
        $('.user_def').css({
            display: 'block',
        })
        $('.user_random').css({
            display: 'none'
        })
        $('.user_elem').css({
            display: 'none',
        })
    } else if(data == 'num_elem'){
        $('.user_def').css({
            display: 'none',
        })
        $('.user_random').css({
            display: 'none'
        })
        $('.user_elem').css({
            display: 'block',
        })
    } else if(data == 'cust_array'){
        $('.user_cust').css({
            display: 'block',
        })
        // $('.user_random').css({
        //     display: 'none'
        // })
        // $('.user_elem').css({
        //     display: 'block',
        // })
    }
}
